import math


def add(x, y):
    return x + y


def subtract(x, y):
    return x - y


def multiply(x, y):
    return x * y


def divide(x, y):
    return x / y


def square(x):
    return x * x


def average(x):
    return sum(x)/len(x)


def cube(x):
    return x * x * x


def sqrt(x):
    return math.sqrt(x)
